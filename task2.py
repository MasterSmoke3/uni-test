import unittest

def test_string_concatenation():
    string_4 = "Как говаривал один мой знакомый"
    string_5 = " по поводу кавычек в питоне: \"Да какая разница, какие использовать? Что вообще может пойти не так?\""
    expected_result = "Как говаривал один мой знакомый по поводу кавычек в питоне: \"Да какая разница, какие использовать? Что вообще может пойти не так?\""
    assert string_4 + string_5 == expected_result

def test_string_repetition():
    string_8 = '-' * 50
    expected_result = "--------------------------------------------------"
    assert string_8 == expected_result

def test_multi_line_string():
    string_7 = '''
Да и в целом то я с ним согласен.
Кажется, что никаких проблем быть не должно.
'''
    expected_result = "\nДа и в целом то я с ним согласен.\nКажется, что никаких проблем быть не должно.\n"
    assert string_7 == expected_result
